#4. Sankey Diagram (Student Flow)
import plotly.graph_objects as go

fig = go.Figure(data=[go.Sankey(
    node=dict(label=["Admission", "First Year", "Second Year", "Placed"]),
    link=dict(
        source=[0, 1, 2],
        target=[1, 2, 3],
        value=[100, 80, 60])
)])

fig.show()

# 5. 3D Scatter Plot (Student Performance)
