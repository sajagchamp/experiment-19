# 6. Radar Chart (Skill Assessment)
import plotly.graph_objects as go

skills = ['Python','ML','DBMS','DSA','Communication']
values = [4,3,5,4,3]

fig = go.Figure()

fig.add_trace(go.Scatterpolar(
    r=values,
    theta=skills,
    fill='toself',
    name='Student Skills'
))

fig.show()